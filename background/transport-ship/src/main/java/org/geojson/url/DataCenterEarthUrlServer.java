package org.geojson.url;
/**
 * 数据中心可视化接口 地址服务
 * @author niuwk
 * @version 1.0
 */
public class DataCenterEarthUrlServer {
	
	private String host;
	private String port;
	
	public String getHost() {
		return host;
	}
	public void setHost(String host) {
		this.host = host;
	}
	public String getPort() {
		return port;
	}
	public void setPort(String port) {
		this.port = port;
	}
	
	

}
